<h1 class="text-center">Historical List of Joint Numerical Weather Prediction Unit (JNWPU) / National Meteorological Center (NMC) / National Centers for Environmental Prediction (NCEP) Numerical Analysis and Prediction Systems (1955-present)</h1>
<p>
<h4>
NOTE: In compiling this list, there has been no intent to include the many changes to individual models that have 
occurred through the years; e.g. changes to numerics, physical processes, etc, as well as the multitude of changes 
that have occurred in producing initial analyses for the numerical models. Here, the intent is to show, in a chronological 
sense, implementations of distinctively new operational models and, in some instances, dates when forecast length 
or horizontal/vertical resolution has been 
increased to satisfy operational requirements. There is an attempt to record dates when models went out of 
service, generally occurring when a new model was implemented. For more details see the references listed below.   
Additional information regarding the 'early' years may be found in 
<a href=http://www.lib.ncep.noaa.gov/ncepofficenotes/files/013FD835.pdf">NMC Office Note 72</a>, by Shuman (1972), entitled 
'The Research and Development Program at the National Meteorological Center'.
</h4>
</p>
<p> 
<h4>
Listed below the table legend are all numerical analysis and weather/ocean prediction systems going back to the
formation of the the JNWPU in 1955. Separate tables are available for <a href="./globalModels.php">Global</a>, 
 <a href="./regionalModels.php">Regional/Mesoscale</a>, <a href="./oceanModels.php">Ocean/Marine</a>, and 
<a href="./hurricaneModels.php">Hurricane</a> systems. 
</h4>
