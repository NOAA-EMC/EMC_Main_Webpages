<p class=MsoBodyText class="text-center" ><b>Historical <a href="modelTable.php" style="font-size:16pt;font-weight:600;color:#ff0000;">list</a> of Atmosphere and Ocean Models at JNWPU/NMC/NCEP</b></p>

<p class=MsoBodyText ><b><o:p>&nbsp;</o:p></b></p>

<p class=MsoNormal>In compiling <a href="modelTable.php" style="font-size:12pt;font-weight:600;color:#ff0000;font-family:sans-serif;">the list</a>, there has been no intent to include the many changes to individual models that have occurred through the years; <i>e.g.</i> changes to numerics, physical processes, <i>etc</i>, as well as the multitude of changes that have occurred in producing initial analyses for the numerical models. There have been a number of other models, such as the Coupled Ocean-Atmosphere Model (circa 1995), which have been run routinely, but not as part of the 'operational suite'. These are NOT included either. Here, the intent is to show, in a chronological sense, implementations of distinctively new operational models and, in some instances, dates when forecast length has been increased to satisfy other operational requirements. There is an attempt to record dates when models went out of service, generally occurring when a new model was implemented. However, there are exceptions (<i>e.g.</i> NGM continues to run in support of MOS)! These will be noted as they are 'discovered'! A more detailed, highly informative, companion to <a href="modelTable.php" style="font-size:12pt;font-weight:600;color:#ff0000;font-family:sans-serif;">this list</a> may be <span class=GramE>found</span> in a <i>Bulletin of the American Meteorological Society</i> paper, co-authored by Kalnay, Lord and McPherson, entitled 'Maturity of Operational Numerical Weather Prediction', December 1998, p 2753. Additional information regarding the 'early' years may be found in NMC Office Note 72, by Shuman, 1972, entitled 'The Research and Development Program at the <st1:place><st1:PlaceName>National</st1:PlaceName> <st1:PlaceName>Meteorological</st1:PlaceName>  <st1:PlaceType>Center</st1:PlaceType></st1:place>'.</p>

