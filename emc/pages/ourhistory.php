                                                                <p style="margin-left:0px; margin-right:0px; text-align:start">&nbsp;</p>
								<p style="margin-left:0px; margin-right:0px; text-align:start">The story of operational Numerical Weather Prediction (NWP) begins in 1954, 
								when the Joint Numerical Weather Prediction Unit (JNWPU) was founded by the NWS and the military weather services. 
								It was the ancestor of EMC, Air Force Global Weather Center and Fleet Numerical Meteorology and Oceanography Center (US Navy). 
								JNWPU began by issuing manually-produced weather prediction products, but the Joint Meteorology Committee (made up of the heads of the weather services) 
								directed JNWPU to pursue operational NWP forecast production. JNWPU installed an IBM 701 computer in March 1955, and chose Charney's Princeton 
								3-level quasi-geostrophic model to be the first operational NWP model. When the model began running operationally later in 1955, 
								the results were very disappointing and not usable by forecasters. JNWPU didn't have an operational NWP model until 1958, when a one-level, 
								improved barotropic version of the Princeton model (with improved numerics, automated objective analysis initial conditions, and octagonal NH domain) 
								was developed. After a new, more powerful IBM 704 computer was installed, JNWPU began using the model operationally to produce useful numerical forecasts.</p>

                                <p style="margin-left:0px; margin-right:0px; text-align:start">&nbsp;</p>
                                
                                <p style="margin-left:0px; margin-right:0px; text-align:start">
                                <img alt="1955: IBM 701 - Peak performance, 1000 operations per second. Sustained performance, 300 operations per second." src="http://www.emc.ncep.noaa.gov/graphics/oldRack5.jpg" style="border:0px solid; box-sizing:border-box; float:left; height:150px; margin:5px; max-width:100%; vertical-align:middle; width:186px" title="1955: IBM 701 - Peak performance, 1000 operations per second. Sustained performance, 300 operations per second.">
                                The JNWPU was co-located with the National Weather Analysis Center (NAWAC) at Suitland, MD from 1955 to 1958. 
                                In January 1958, JNWPU and NAWAC merged and formed the National Meteorological Center (NMC), the direct predecessor of NCEP, 
                                while the USAF and Navy formed their own global prediction centers (in Omaha and Monterey) to support military operations. 
                                NMC at once became the "nerve center" for weather data in the United States. NMC processed weather observations from around the globe and 
                                disseminated analyses and forecasts to customers throughout the U.S. and other countries. Research increased, with emphasis on developing 
                                faster and more accurate numerical techniques. It was the only such facility in the world at the time, and at least one publication described 
                                its creation as being "a milestone in the progress of meteorology." In January 1975, NMC (except for the supercomputer facility) moved to the 
                                World Weather Building (also known as the NOAA Science Center) in Camp Springs MD.</p>
                                
