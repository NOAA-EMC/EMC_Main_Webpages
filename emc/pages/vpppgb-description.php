	<li style="margin-left: 0.83in;"><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:16px;">Evaluate new model systems, in partnership with internal and external users, led by <a href="https://www.emc.ncep.noaa.gov/users/meg/home/">EMC’s Model Evaluation Group</a></span></span></li>
