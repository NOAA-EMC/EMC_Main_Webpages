<p><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:16px;">The Verification, Post-processing and Product Generation Branch (VPPPGB) team works collaboratively with the community to conduct diagnostic verification studies of model performance on weather and climate time and space scales; diagnostic studies to evaluate potential improvements in forecast skill; production of deterministic and ensemble products using models from EMC; and, post-processing of model output for use by the global weather community. The VPPPGB publishes research results in various media for dissemination to the world oceanographic and meteorological community, and supports the development and implementation of the <a href="https://ufscommunity.org">Unified Forecast System</a>. The VPPPGB uses the <a href="https://dtcenter.org/community-code/model-evaluation-tools-met">METplus</a> software for verification and evaluation, the <a href="https://dtcenter.org/community-code/unified-post-processor-upp">Unified Post Processor (UPP)</a> software for model post-processing, and other custom software tools.</span></span></p>

