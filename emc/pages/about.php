                                    <p style="margin-left:0px; margin-right:0px">
					The Environmental Modeling Center (EMC) is a premier developer of Numerical Weather Prediction systems. Our mission is to develop, enhance and maintain numerical forecast systems for the atmosphere, hydrosphere, and cryosphere in support of national and international forecast requirements. We accomplish this through close collaboration with an extensive network of partners from the academic, federal and commercial sectors. We are one of nine centers in the National Centers for Environmental Prediction (NCEP). NCEP, an arm of NOAA's National Weather Service (NWS), provides a wide variety of national and international weather guidance products to NWS field offices, government agencies, emergency managers, private sector meteorologists and oceanographers, and meteorological/oceanographic organizations and societies throughout the world. Together with the other centers in NCEP, EMC is a critical national resource in national and global weather prediction and is a starting point for nearly all weather forecasts in the United States.
