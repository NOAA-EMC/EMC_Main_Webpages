<p style="margin-left:0px; margin-right:0px; text-align:center"><img src="https://www.emc.ncep.noaa.gov/emc/images/forcast_system_icons/animated/gefs_animated.gif" style="border: 0px none; box-sizing: border-box; vertical-align: middle;"></p>


<p style="margin-left:0px; margin-right:0px"><span style="font-family:arial,helvetica,sans-serif;"><span style="font-size:12px;">To illustrate the use of ensemble based probabilistic forecast, the relative measure of predictability (RMOP, Toth et al. 2003), is shown in the above animation from the GEFS run from 0000 UTC 4 November 2020, out to 15 days (360 hours). Considering the 120-h forecast valid at 0000 UTC 9 November 2020, the shading indicates the RMOP of the ensemble mean 500-hPa height at each grid point, compared to ensemble forecasts of 500-hPa height over the previous 30 days. These are in 10% increments as indicated by the color bar at the bottom of the graphic. Shading at 90% indicates that at least 9 of 10 ensemble forecasts in the past 30 days had fewer ensemble members in the same "bin" as the ensemble mean than the present forecast. The blue numbers over each box represents the percentage of time that a forecast with the given degree of predictability has verified over the past 30 days. Here, over the 90% predictability box we see that only 61-84% of the forecasts with 90% relative predictability at 120 hours have verified in the same climatological bin as the observed 500-hPa height at 120 hours over the past 30 days. Note that in general, the values are generally lower than the RMOP numbers below the bar. This is because the underlying forecast model is imperfect, the initial conditions are imprecise, and the atmosphere behaves chaotically.</span></span><br>

<p class="western" style="margin-top: 0.04in; margin-bottom: 0.08in; line-height: 100%; page-break-before: always" lang="en-GB" align="justify"><span style="font-size:16px;"><span style="font-family:arial,helvetica,sans-serif;"><span lang="en-US"><b>1. Introduction / History</b></span></span></span></p>

<p class="teaser" style="margin: 0px 0px 1.5em; padding: 0px; border: 0px none; outline: 0px none; background: transparent none repeat scroll 0% 0%; vertical-align: baseline; font-family: &quot;Open Sans&quot;,Helvetica,Verdana,Arial,sans-serif; color: rgb(68, 68, 68); line-height: 1.556em; text-rendering: auto;"><span style="font-size:16px;"><span style="font-family:arial,helvetica,sans-serif;">The Global Ensemble Forecast System (GEFS) has been operational at NCEP since December 1992, with the initial version using the NCEP Global Spectral Model (GSM) at T62L18 resolution (about 200km in horizontal and 18 vertical sigma levels) and the initial condition perturbations (2 pairs perturbed and 1 control members) were generated by breeding vector (BV) method (Toth and Kalnay 1993; Toth and Kalnay 1997; Toth et al. 1997; Toth et al. 2001; Zhu et al. 2002; Buizza et al. 2005; Zhu 2005). The GEFS ran once per day, out to 12 days in the early 90s. During the early 2000s, the 1st generation of GEFS reforecast (1979 - 2006) was produced off-line from using NCEP GFS/GEFS 1998 model version by NOAA PSL (Hamill et al. 2006) to demonstrate the improved ensemble reliability through bias correction and calibration.</span></span></p>

<p class="teaser" style="margin: 0px 0px 1.5em; padding: 0px; border: 0px none; outline: 0px none; background: transparent none repeat scroll 0% 0%; vertical-align: baseline; font-family: &quot;Open Sans&quot;,Helvetica,Verdana,Arial,sans-serif; color: rgb(68, 68, 68); line-height: 1.556em; text-rendering: auto;"><span style="font-size:16px;"><span style="font-family:arial,helvetica,sans-serif;">In August 2005, the GEFS was upgraded to run four times per day (0000, 0600, 1200 and 1800 UTC) and produced forecasts out to 16 days. Each of these cycles have 10 (5 pairs) perturbed members that were initialized using BV method and cycled every 6 hours. If tropical cyclones were present in the initial times, a tropical storm relocation (TSR) technique was applied to each ensemble member to adjust the initial central location to the observed location (Liu et al. 2006). An extended BV method with Ensemble Transform and Rescaling (BV-ETR) (Wei et al. 2006; 2008) and 6-hour cycling was implemented operationally in 2006.</span></span></p>

<p class="teaser" style="margin: 0px 0px 1.5em; padding: 0px; border: 0px none; outline: 0px none; background: transparent none repeat scroll 0% 0%; vertical-align: baseline; font-family: &quot;Open Sans&quot;,Helvetica,Verdana,Arial,sans-serif; color: rgb(68, 68, 68); line-height: 1.556em; text-rendering: auto;"><span style="font-size:16px;"><span style="font-family:arial,helvetica,sans-serif;">In early 2010, the GEFS was upgraded with enhanced representation of model uncertainty using the Stochastic Total Tendency Perturbation (STTP) algorithm (Hou et al., 2008). The stochastic tendency perturbations were updated every 6 hours. Meanwhile, the  2nd generation of NOAA GEFS reforecasts were produced off-line for 29 years (1985 - 2013) by NOAA PSL (Hamill et al. 2013; <a href="https://psl.noaa.gov/forecasts/reforecast2/">NOAA/PSL reforecast website</a>) using GEFS v10 configurations and CFS reanalysis.</span></span></p>

<p class="teaser" style="margin: 0px 0px 1.5em; padding: 0px; border: 0px none; outline: 0px none; background: transparent none repeat scroll 0% 0%; vertical-align: baseline; font-family: &quot;Open Sans&quot;,Helvetica,Verdana,Arial,sans-serif; color: rgb(68, 68, 68); line-height: 1.556em; text-rendering: auto;"><span style="font-size:16px;"><span style="font-family:arial,helvetica,sans-serif;">Through another major upgrade in December 2015, the GEFS initial perturbations were chosen from the operational hybrid Global Data Assimilation System (GDAS) 80-member Ensemble Kalman Filter (EnKF; Whitaker et al., 2008) 6-h forecasts along with tropical storm relocation and centralization of the initial perturbations (Zhou et al. 2016; 2017).</span></span></p>

