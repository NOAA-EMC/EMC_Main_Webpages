<p>The fundamental mission of the NCEP/NWS Environmental Modeling Center (EMC), in close collaboration with our partners and stakeholders, is to maintain, enhance and transition-to-operations advanced numerical guidance systems for the Nation's weather/water/climate enterprise and the global community for the protection of life/property and the enhancement of the economy. To that end, and reflecting our vision to be one of the world's best and most trusted providers of numerical guidance, EMC is committed to a multi-year plan to use the NOAA Next Generation Global Prediction System (<a href="https://www.weather.gov/sti/stimodeling_nggps">NGGPS</a>) as to foundation upon which to build a <a href="https://ufscommunity.org">Unified Forecast System </a> for the future NCEP Production Suite. The envisioned end state would be a simplified production suite under this unified framework using a single dynamic core for all temporal and spatial model scales which is fully coupled and ensemble-based. By its active participation in the NGGPS Strategic Implementation Plan Working Groups, EMC is fully engaged with the nation's weather/water/climate community in all aspects of the project, from research/development to testing and validation.</p>

<p>A high-level overview of EMC's vision and how it adheres to the overarching NOAA Unified Modeling framework is outlined in the <a href="https://www.emc.ncep.noaa.gov/emc/docs/02-EMC_overview_NPSR_20171204.pdf">EMC Director's presentation</a> at the 2017 NCEP Production Suite Review on December 4, 2017. Short-term activities are outlined in the 2019-2021 <a href="https://www.weather.gov/media/sti/nggps/UFS%20SIP%20FY19-21_20181129.pdf" target="_blank">UFS/NGGPS Implementation Plan</a>, which describes the major EMC and community development and implementation projects for this time frame.</p>
